    def transplant_aneurysm_with_overlap_constraint(
        self,
        target_vessel_mask,
        source_aneurysm_mask,
        bone_mask,
        min_overlap_ratio=0.05,
        max_overlap_ratio=0.30,
        max_trials=50,
        max_bone_ratio=0.02
        ):
        # ---------- 1. extract one aneurysm CC ----------
        labeled, num_cc = label(source_aneurysm_mask > 0)
        if num_cc == 0:
            raise ValueError("No aneurysm found")
    
        chosen_label = self.rng.integers(1, num_cc + 1)
        aneurysm_cc = (labeled == chosen_label)
        aneurysm_volume = np.sum(aneurysm_cc)
    
        if aneurysm_volume == 0:
            raise ValueError("Empty aneurysm component")
    
        # ---------- 2. candidate vessel points ----------
        vessel_points = np.argwhere(target_vessel_mask > 0)
        if len(vessel_points) == 0:
            raise ValueError("Target vessel empty")
    
        aneurysm_points = np.argwhere(aneurysm_cc)
        aneurysm_center = aneurysm_points.mean(axis=0)
    
        best_mask = None
        best_ratio_diff = np.inf
    
        # ---------- 3. try multiple placements ----------
        for _ in range(max_trials):
            vessel_center = vessel_points[self.rng.integers(len(vessel_points))]
            shift = (vessel_center - aneurysm_center).astype(int)
    
            shifted_coords = aneurysm_points + shift
            valid = np.all(
                (shifted_coords >= 0) &
                (shifted_coords < np.array(target_vessel_mask.shape)),
                axis=1
            )
            shifted_coords = shifted_coords[valid]
    
            new_mask = np.zeros_like(target_vessel_mask, dtype=np.uint8)
            new_mask[
                shifted_coords[:, 0],
                shifted_coords[:, 1],
                shifted_coords[:, 2]
            ] = 1
    
            overlap = np.sum((new_mask > 0) & (target_vessel_mask > 0))
            overlap_ratio = overlap / aneurysm_volume
            
            bone_overlap = np.sum((new_mask > 0) & (bone_mask > 0))
            bone_ratio = bone_overlap / aneurysm_volume
    
            # ---------- 4. check constraint ----------
            if bone_ratio > max_bone_ratio:
                continue
            
            if min_overlap_ratio <= overlap_ratio <= max_overlap_ratio:
                return new_mask
    
            # keep closest for fallback
            ratio_diff = min(
                abs(overlap_ratio - min_overlap_ratio),
                abs(overlap_ratio - max_overlap_ratio)
            )
            if ratio_diff < best_ratio_diff:
                best_ratio_diff = ratio_diff
                best_mask = new_mask
    
        # ---------- 5. fallback ----------
        return best_mask
        
    
    def transplant_aneurysm_cc_to_target(
        self,
        source_aneurysm_mask,
        target_shape,
        target_coord,
        rng=None
    ):
        """
        Transplant ONE aneurysm connected component from a source mask
        into a target volume by translating its center of mass to a given
        target coordinate.
    
        Args:
            source_aneurysm_mask (np.ndarray):
                Binary or multi-label aneurysm mask (source space).
            target_shape (tuple or np.ndarray):
                Shape of the target volume, e.g. (X, Y, Z).
            target_coord (array-like):
                Target coordinate (x, y, z) in target space.
            rng (np.random.Generator, optional):
                Random generator (used only if multiple CCs exist).
    
        Returns:
            new_mask (np.ndarray):
                Binary aneurysm mask in target space with shape = target_shape.
        """
    
        from scipy.ndimage import label
    
        # -------------------------------
        # 1. extract connected components
        # -------------------------------
        labeled, num_cc = label(source_aneurysm_mask > 0)
        if num_cc == 0:
            raise ValueError("No aneurysm component found in source mask")
    
        # pick one CC (random if multiple)
        if num_cc == 1:
            chosen_label = 1
        else:
            if rng is None:
                chosen_label = 1
            else:
                chosen_label = rng.integers(1, num_cc + 1)
    
        aneurysm_cc = (labeled == chosen_label)
    
        # -------------------------------
        # 2. get aneurysm coordinates
        # -------------------------------
        aneurysm_points = np.argwhere(aneurysm_cc)
        if len(aneurysm_points) == 0:
            raise ValueError("Selected aneurysm CC is empty")
    
        aneurysm_center = aneurysm_points.mean(axis=0)
    
        # -------------------------------
        # 3. compute translation
        # -------------------------------
        target_coord = np.asarray(target_coord, dtype=float)
        shift = np.round(target_coord - aneurysm_center).astype(int)
    
        # -------------------------------
        # 4. apply translation
        # -------------------------------
        shifted_coords = aneurysm_points + shift
    
        # -------------------------------
        # 5. clip to target volume
        # -------------------------------
        target_shape = np.asarray(target_shape)
        valid = np.all(
            (shifted_coords >= 0) &
            (shifted_coords < target_shape),
            axis=1
        )
        shifted_coords = shifted_coords[valid]
    
        # -------------------------------
        # 6. write into target canvas
        # -------------------------------
        new_mask = np.zeros(target_shape, dtype=np.uint8)
        new_mask[
            shifted_coords[:, 0],
            shifted_coords[:, 1],
            shifted_coords[:, 2]
        ] = 1
    
        return new_mask
        
    
