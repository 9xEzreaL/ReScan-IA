    def _random_crop_with_mask(self, image, mask, seg_mask, vessel_mask=None):
        """
        Random crop that ensures the crop region contains at least part of the mask.
        
        Args:
            image: numpy array of shape (X, Y, Z) = (width, height, depth)
            mask: numpy array of shape (X, Y, Z) = (width, height, depth) - inpainting mask
            seg_mask: numpy array of shape (X, Y, Z) = (width, height, depth) - segmentation mask (for SPADE)
            vessel_mask: numpy array of shape (X, Y, Z) = (width, height, depth) - vessel segmentation mask (optional)
        
        Returns:
            cropped_image: numpy array of shape (X, Y, Z) = (crop_width, crop_height, crop_depth)
            cropped_mask: numpy array of shape (X, Y, Z) = (crop_width, crop_height, crop_depth)
            cropped_seg_mask: numpy array of shape (X, Y, Z) = (crop_width, crop_height, crop_depth)
            cropped_vessel_mask: numpy array of shape (X, Y, Z) = (crop_width, crop_height, crop_depth) or None
        """
        width, height, depth = image.shape  # (X, Y, Z)
        crop_d, crop_h, crop_w = self.image_size  # (D, H, W) = (Z, Y, X)
        
        # If image is smaller than crop size, pad or use entire image
        if depth < crop_d or height < crop_h or width < crop_w:
            # Option 1: Pad to crop size
            pad_d = max(0, crop_d - depth)
            pad_h = max(0, crop_h - height)
            pad_w = max(0, crop_w - width)
            
            pad_d_before = pad_d // 2
            pad_d_after = pad_d - pad_d_before
            pad_h_before = pad_h // 2
            pad_h_after = pad_h - pad_h_before
            pad_w_before = pad_w // 2
            pad_w_after = pad_w - pad_w_before
            
            image = np.pad(image, ((pad_w_before, pad_w_after), (pad_h_before, pad_h_after), (pad_d_before, pad_d_after)), 
                          mode='constant', constant_values=0)
            mask = np.pad(mask, ((pad_w_before, pad_w_after), (pad_h_before, pad_h_after), (pad_d_before, pad_d_after)), 
                         mode='constant', constant_values=0)
            seg_mask = np.pad(seg_mask, ((pad_w_before, pad_w_after), (pad_h_before, pad_h_after), (pad_d_before, pad_d_after)), 
                             mode='constant', constant_values=0)
            if vessel_mask is not None:
                vessel_mask = np.pad(vessel_mask, ((pad_w_before, pad_w_after), (pad_h_before, pad_h_after), (pad_d_before, pad_d_after)), 
                                    mode='constant', constant_values=0)
            width, height, depth = image.shape
        
        # Find all mask positions (non-zero voxels) - prefer seg_mask if available
        mask_positions = np.where(seg_mask > 0) if np.any(seg_mask > 0) else np.where(mask > 0)
        
        if len(mask_positions[0]) == 0:
            # No mask found, do regular random crop
            x_start = np.random.randint(0, max(1, width - crop_w + 1))
            y_start = np.random.randint(0, max(1, height - crop_h + 1))
            z_start = np.random.randint(0, max(1, depth - crop_d + 1))
        else:
            # Ensure crop contains at least part of the mask
            # Strategy: Randomly select a mask point, then ensure it's within the crop region
            mask_idx = np.random.randint(0, len(mask_positions[0]))
            mask_x = mask_positions[0][mask_idx]
            mask_y = mask_positions[1][mask_idx]
            mask_z = mask_positions[2][mask_idx]
            
            center_low = 1.0 / 3.0
            center_high = 2.0 / 3.0

            def compute_start(mask_pos, crop_size, full_size):
                min_start = int(mask_pos - crop_size * center_high)
                max_start = int(mask_pos - crop_size * center_low)

                # clip to valid range
                min_start = max(0, min_start)
                max_start = min(full_size - crop_size, max_start)

                if min_start > max_start:
                    # fallback: center crop
                    center_start = int(mask_pos - crop_size // 2)
                    center_start = max(0, min(full_size - crop_size, center_start))
                    return center_start
                return np.random.randint(min_start, max_start + 1)

            x_start = compute_start(mask_x, crop_w, width)
            y_start = compute_start(mask_y, crop_h, height)
            z_start = compute_start(mask_z, crop_d, depth)
            
            
        
        # Perform crop: (X, Y, Z) format
        cropped_image = image[x_start:x_start + crop_w, 
                             y_start:y_start + crop_h, 
                             z_start:z_start + crop_d]
        cropped_mask = mask[x_start:x_start + crop_w, 
                           y_start:y_start + crop_h, 
                           z_start:z_start + crop_d]
        cropped_seg_mask = seg_mask[x_start:x_start + crop_w, 
                                    y_start:y_start + crop_h, 
                                    z_start:z_start + crop_d]
        cropped_vessel_mask = None
        if vessel_mask is not None:
            cropped_vessel_mask = vessel_mask[x_start:x_start + crop_w, 
                                              y_start:y_start + crop_h, 
                                              z_start:z_start + crop_d]
        
        return cropped_image, cropped_mask, cropped_seg_mask, cropped_vessel_mask
    
    def __getitem__(self, index):
        sample = self.samples[index]
        file_path = sample['file_path']
        mask_path = sample['mask_path']
        vessel_path = sample.get('vessel_path', None)
        
        # Load NIfTI image
        nii_img = nib.load(file_path)
        image_data = nii_img.get_fdata()
        
        # Load binary mask (segmentation map for SPADE)
        nii_mask = nib.load(mask_path)
        seg_mask_data = nii_mask.get_fdata()
        
        # Load vessel segmentation (optional)
        vessel_mask_data = None
        if vessel_path is not None:
            nii_vessel = nib.load(vessel_path)
            vessel_mask_data = nii_vessel.get_fdata()
        
        # Handle different data types and orientations
        # Convert to numpy array if needed
        if not isinstance(image_data, np.ndarray):
            image_data = np.array(image_data)
        if not isinstance(seg_mask_data, np.ndarray):
            seg_mask_data = np.array(seg_mask_data)
        if vessel_mask_data is not None and not isinstance(vessel_mask_data, np.ndarray):
            vessel_mask_data = np.array(vessel_mask_data)
        
        # Ensure seg_mask is binary (0 or 1)
        seg_mask_data = (seg_mask_data > 0).astype(np.float32)
        
        # Ensure vessel_mask is binary (0 or 1) if present
        if vessel_mask_data is not None:
            vessel_mask_data = (vessel_mask_data > 0).astype(np.float32)
            vessel_mask_data = vessel_mask_data * (1.0 - seg_mask_data)
        
        # Get image shape - NIfTI format from nibabel is typically (width, height, depth) = (X, Y, Z)
        if len(image_data.shape) == 3:
            width, height, depth = image_data.shape  # (X, Y, Z)
        else:
            raise ValueError(f"Expected 3D image, got shape: {image_data.shape}")
        
        # Check that seg_mask has same shape as image
        if seg_mask_data.shape != image_data.shape:
            print(file_path)
            raise ValueError(f"Segmentation mask shape {seg_mask_data.shape} does not match image shape {image_data.shape}")
        
        # Check that vessel_mask has same shape as image if present
        if vessel_mask_data is not None and vessel_mask_data.shape != image_data.shape:
            raise ValueError(f"Vessel segmentation mask shape {vessel_mask_data.shape} does not match image shape {image_data.shape}")
        
        # Normalize CT image
        if self.normalization == 'nnunet':
            image_normalized = ct_normalize_nnunet(
                image_data, 
                foreground_percentiles=self.foreground_percentiles,
                global_mean=self.global_mean,
                global_std=self.global_std,
                use_nonzero_only=True
            )
        elif self.normalization == 'nn_diffusion':
            image_normalized = ct_normalize_for_diffusion(
                image_data, 
                foreground_percentiles=self.foreground_percentiles,
                global_mean=self.global_mean,
                global_std=self.global_std,
                use_nonzero_only=True
            )
        else:
            # Use simple normalization
            image_normalized = ct_normalize_simple(image_data, self.hu_min, self.hu_max)
        
        # Generate random inpainting mask (for training) using original method
        # Randomly select a position within the seg_mask region and create cuboid or sphere
        mask = np.zeros((width, height, depth), dtype=np.uint8)
        
        # Find non-zero regions in seg_mask to place inpainting mask
        seg_positions = np.where(seg_mask_data > 0)
        
        if len(seg_positions[0]) > 0:
            # Randomly select a position within the segmentation region
            pos_idx = np.random.randint(0, len(seg_positions[0]))
            center_x = int(seg_positions[0][pos_idx])
            center_y = int(seg_positions[1][pos_idx])
            center_z = int(seg_positions[2][pos_idx])
            
            # Randomly select mask size
            size_x = np.random.randint(self.mask_size_range[0], self.mask_size_range[1] + 1)
            size_y = np.random.randint(self.mask_size_range[0], self.mask_size_range[1] + 1)
            size_z = np.random.randint(self.mask_size_range[0], self.mask_size_range[1] + 1)
            
            # 50% probability: use cuboid, 50%: use sphere
            use_sphere = np.random.rand() >= 0
            
            if use_sphere:
                # Use spherical mask
                radius = np.mean([size_x, size_y, size_z]) / 2.0
                mask = sphere3d_to_mask(
                    img_shape=(width, height, depth),
                    center_x=center_x,
                    center_y=center_y,
                    center_z=center_z,
                    radius=radius
                )
            else:
                # Use cuboid mask
                mask = bbox3d_to_mask(
                    img_shape=(width, height, depth),
                    center_x=center_x,
                    center_y=center_y,
                    center_z=center_z,
                    size_x=size_x,
                    size_y=size_y,
                    size_z=size_z
                )
        
        # Apply random crop if image_size is specified
        if self.image_size is not None:
            crop_result = self._random_crop_with_mask(
                image_normalized, mask, seg_mask_data, vessel_mask_data
            )
            image_normalized, mask, seg_mask_data, vessel_mask_data = crop_result
            # Update dimensions after crop
            width, height, depth = image_normalized.shape
        
        # Convert to torch tensor: shape is (X, Y, Z) = (width, height, depth)
        image_tensor = torch.from_numpy(image_normalized).float()
        
        # Permute to (Z, Y, X) = (depth, height, width) for 3D network
        # 3D UNet expects (C, D, H, W) format where D is depth/slice dimension
        image_tensor = image_tensor.permute(2, 1, 0)  # (X, Y, Z) -> (Z, Y, X) = (D, H, W)
        
        # Add channel dimension: (1, D, H, W) = (1, Z, Y, X)
        image_tensor = image_tensor.unsqueeze(0)
        
        # Convert inpainting mask to torch tensor: shape is (X, Y, Z) = (width, height, depth)
        mask_tensor = torch.from_numpy(mask).float()
        
        # Permute to (Z, Y, X) = (depth, height, width) to match image_tensor
        mask_tensor = mask_tensor.permute(2, 1, 0)  # (X, Y, Z) -> (Z, Y, X) = (D, H, W)
        
        # Add channel dimension: (1, D, H, W) = (1, Z, Y, X)
        mask_tensor = mask_tensor.unsqueeze(0)
        
        # Convert seg_mask to torch tensor: shape is (X, Y, Z) = (width, height, depth)
        seg_mask_tensor = torch.from_numpy(seg_mask_data).float()
        
        # Permute to (Z, Y, X) = (depth, height, width) to match image_tensor
        seg_mask_tensor = seg_mask_tensor.permute(2, 1, 0)  # (X, Y, Z) -> (Z, Y, X) = (D, H, W)
        
        # Add channel dimension: (1, D, H, W) = (1, Z, Y, X)
