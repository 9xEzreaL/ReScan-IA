        nifti_files = make_nifti_dataset(data_root)
        
        # Load binary mask files from aneurysm_root
        aneurysm_files = make_nifti_dataset(aneurysm_root)
        
        # Create mapping from UID to file paths
        uid_to_image = {}
        for nifti_file in nifti_files:
            uid = extract_series_uid(nifti_file)
            if uid not in uid_to_image:
                uid_to_image[uid] = nifti_file
        
